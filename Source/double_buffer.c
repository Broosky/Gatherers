IPOINT_T __cdecl DOUBLE_BUFFER_FindBlitterPoint(char cChar) {
    UINT8 ubI;
    IPOINT_T CurrentPoint = { 0, 0 };
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // abcdefghijklmnopqrstuvwxyz
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    for (ubI = 97; ubI <= 122; ubI++) {
        if (cChar == (char)ubI) {
            return CurrentPoint;
        }
        CurrentPoint.iX += 1;
    }
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // ABCDEFGHIJKLMNOPQRSTUVWXYZ
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    CurrentPoint.iX = 0;
    CurrentPoint.iY = 1;
    for (ubI = 65; ubI <= 90; ubI++) {
        if (cChar == (char)ubI) {
            return CurrentPoint;
        }
        CurrentPoint.iX += 1;
    }
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // 0123456789
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    CurrentPoint.iX = 0;
    CurrentPoint.iY = 2;
    for (ubI = 48; ubI <= 57; ubI++) {
        if (cChar == (char)ubI) {
            return CurrentPoint;
        }
        CurrentPoint.iX += 1;
    }
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //  !"#$%&'()*+,-./ (The first character is a space.)
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    CurrentPoint.iX = 0;
    CurrentPoint.iY = 3;
    for (ubI = 32; ubI <= 47; ubI++) {
        if (cChar == (char)ubI) {
            return CurrentPoint;
        }
        CurrentPoint.iX += 1;
    }
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // :;<=>?@
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    CurrentPoint.iX = 0;
    CurrentPoint.iY = 4;
    for (ubI = 58; ubI <= 64; ubI++) {
        if (cChar == (char)ubI) {
            return CurrentPoint;
        }
        CurrentPoint.iX += 1;
    }
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // [\]^_`
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    CurrentPoint.iX = 0;
    CurrentPoint.iY = 5;
    for (ubI = 91; ubI <= 96; ubI++) {
        if (cChar == (char)ubI) {
            return CurrentPoint;
        }
        CurrentPoint.iX += 1;
    }
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // {|}~
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    CurrentPoint.iX = 0;
    CurrentPoint.iY = 6;
    for (ubI = 123; ubI <= 126; ubI++) {
        if (cChar == (char)ubI) {
            return CurrentPoint;
        }
        CurrentPoint.iX += 1;
    }
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Character not found.
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    CurrentPoint.iX = 0;
    CurrentPoint.iY = 0;
    return CurrentPoint;
}
